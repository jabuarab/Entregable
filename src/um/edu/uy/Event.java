package um.edu.uy;

public class Event {
    String name;

    Sport deporte;

    public Event() {
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Sport getDeporte() {
        return deporte;
    }

    public void setDeporte(Sport deporte) {
        this.deporte = deporte;
    }
}
